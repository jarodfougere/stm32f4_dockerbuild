static struct peripherals peripherals = 
{
    .USB = (struct USB_OTG*)(USB_OTG_FS_PERIPH_BASE),
    .adc_common = (struct adc_common*)(ADC1_COMMON_BASE),
    .adc = (struct adc_periph*)(ADC1_BASE),
    .iwdg = (struct iwdg_regs*)(IWDG_BASE),
    .rtc = (struct rtc_regs*)(RTC_BASE),
    .dma2 = (struct dma_peripheral*)(DMA2_BASE),
    .dma1 = (struct dma_peripheral*)(DMA1_BASE),
    .flash_controller = (struct flash_controller*)(FLASH_R_BASE),
    .rcc = (struct rcc_regs*)(RCC_BASE),
    .gpioH = (struct gpio_periph*)(GPIOH_BASE),
    .gpioE = (struct gpio_periph*)(GPIOE_BASE), 
    .gpioD = (struct gpio_periph*)(GPIOD_BASE),
    .gpioC = (struct gpio_periph*)(GPIOC_BASE),
    .gpioB = (struct gpio_periph*)(GPIOB_BASE),
    .gpioA = (struct gpio_periph*)(GPIOA_BASE),
};



static struct peripherals *periph(void)
{
    return &peripherals;
}

